def detect_skills(question: str) -> list[str]:
    """Определяет активные скиллы по ключевым словам"""
    q_lower = question.lower()
    active = []
    for skill_id, data in SKILLS.items():
        for kw in data["keywords"]:
            if kw in q_lower:
                active.append(skill_id)
                break # Один матч на скилл = достаточно
    return active

def get_skills_prompt(skill_ids: list[str]) -> str:
    """Собирает итоговый блок инструкций для промпта"""
    parts = []
    for sid in skill_ids:
        if sid in SKILLS:
            parts.append(SKILLS[sid]["instruction"])
    return "\n".join(parts) if parts else ""